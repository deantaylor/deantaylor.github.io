		<h5>BRIEF</h5>

		<p>A screen-based experience anticipating future technologies within Dundee’s City Square. Our concept was to offer something
			to groups who are visiting Dundee.</p>
			<h5>OBSERVATIONS</h5>

			<p>As this experience was to take place in Dundee’s City Square, there would be no better place to note down some observations.
				Individually, we observed the environment and the commuters who were engaging with the square.
				<br><br>
				The square is a large area, with shopfronts such as a travel agent and many council buildings, one of which is an information centre. It was noted, that most of Dundee’s patrons took no notice of the square and used it only as a shortcut to access other parts of the city centre. Those that were engaging with the square were primarily surrounding the landmark fountains and tossing coins.
				<br><br>
				We also gathered stories from groups around the city, who may have used the square, and more specifically the fountains. We gathered this information and created user profiles.
			</p>

			<img src="/images/wall.png">
			<h5>IDEAS</h5>

			<p>We wanted to concentrate on the interactions we observed with the fountains. ‘Making a wish’ was a broad direction we aimed for. This offered an opportunity to develop a pre-existing pastime in Dundee and make it a digital experience, assuming these future technologies. 
				<br><br>We discussed ways we can integrate the smartphone into the process, as already we use our phones our ‘digital advisor’. We decided to treat the fountain as an extension of that journey, and use the action of tossing the coin as a sort of initiation. The fountain would assume a screen under the water bed to display relevant information. <br><br>
				We reflected on our insights from our visits to the square. We considered the need for group participation and developed the concept with this in mind.
</p>

				<h5>CONCEPT</h5> <p>
				A group would approach the fountain, and individually connect with their smartphone. They can type in ‘keywords’ that tell Infowish what they are looking for in their activities. Once each member of the group has entered their keywords and flicked in their digital coin, Infowish will feedback suggestions on how to spend their day.

				
				
			</p>

			<h5>USER JOURNEY</h5>

			<p>We designed the journey to have as few steps as possible before receiving the suggestion from the fountain. As it is a group activity, we found it to be important, that each user was to understand the result of another users engagement. Our solution was to appoint each participants choices onto a digital coin, that could then be flicked and visualised within the fountain. These would then merge into one larger coin before displaying a suggestion.
			</p>
			<h5>PROTOTYPING</h5>

			<p>We built a scale model of the fountain out of cardboard and aluminium rods. This was, at the time, a comical approach, yet it offered an excellent environment for prototyping. 
