<p>Develop a concept for a mobile banking app that offers a solution to a specific problem
This brief was partly directed by a major bank.</p>
<h5>RESEARCH</h5> 

<p>I wanted to concentrate on the banking practices of charities and small organisation. I organised some time with some Scottish charities to gather an understanding of how their finances were organised. This gave me an opportunity to discuss potential issues or experiences they may have had in light of their banking. </p>

<img src="/images/insights_sim.jpg" alt="">

<p>A concise summary of insights and observations can be found in this short research report found <a href="/files/social_mobile_research.pdf">here</a>.</p>

<p>Both organisations were quick to identify problems regarding payment authorisations and signatories amongst other things. Due to the emphasis the participants put on the issue I decided to focus my efforts in this area.  </p>

<img src="/images/sm_noted.jpg" alt="">

<p>I was able to identify a problem that I could offer potential creative solutions to.</p>

<p><i>‘Authorising payments is a difficult and timely task. It uses complicated software and in some cases, additional hardware’.</i></p>

<h5>CONCEPT GENERATION</h5>

Research showed strong evidence that the main issues with the current authorisation system were both the time and the complicated systems in which were used. These systems are often small parts of much larger systems. The experience of authorising payments could almost be seen as an after thought. It is immediately apparent that these systems were developed purely with the considerations of the bank at mind, leaving little thought to customer experience.



